# WET(We Enjoy Typing)

def calculate_area_of_rectangle(width, height):
    area = width * height
    print("Alan:", area)

def calculate_perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    print("Çevre:", perimeter)

def calculate_area_of_square(side_length):
    area = side_length * side_length
    print("Alan:", area)

def calculate_perimeter_of_square(side_length):
    perimeter = 4 * side_length
    print("Çevre:", perimeter)

calculate_area_of_rectangle(5, 10)
calculate_perimeter_of_rectangle(5, 10)
calculate_area_of_square(4)
calculate_perimeter_of_square(4)
